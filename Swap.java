public class Swap {

	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		System.out.println("before swaping"+a);
		System.out.println("before swapping"+b);
		int temp;
		temp=a;
		a=b;
		b=temp;
		System.out.println("after swaping"+a);
		System.out.println("after swapping"+b);

	}

}
