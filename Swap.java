public class Swap {

	public static void main(String[] args) {
		
		int a=100;
		int b=200;
		System.out.println("before swaping a is"+a);
		System.out.println("before swapping b is"+b);
		int temp;
		temp=a;
		a=b;
		b=temp;
		System.out.println("after swaping a is"+a);
		System.out.println("after swapping b is"+b);

	}

}
